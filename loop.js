

// for loop practice..

for (var i = 0 ; i < 10; i++) {
    document.write("<br>" + i);
    
}

document.write("<br>REST OF THE CODE");

//  continue statement 

document.write("<br><br><br>");
document.write("CONTINUE STATEMENT ")
 
for ( var i = 0 ; i < 10 ; i++)
{
    if (i == 3) {
        continue;
    }
    document.write("<br>" + i);
}

//  block statement .


document.write("<br><br><br>");
document.write("BREAK STATEMENT ")
 
for ( var i = 0 ; i < 10 ; i++)
{
    if (i == 3) {
        break;
    }
    document.write("<br>" + i);
}